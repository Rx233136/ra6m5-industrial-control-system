#include "bsp_led.h"

/*******************************************************************************************************************//**
 * @brief  LED初始化，设置默认熄灭状态
**********************************************************************************************************************/
void BSP_LED_Init(void)
{
    // 上电默认输出高电平，LED熄灭
    R_IOPORT_PinWrite(&g_ioport_ctrl, LED_PIN, LED_OFF);
}

/*******************************************************************************************************************//**
 * @brief  翻转LED亮灭状态
**********************************************************************************************************************/
void BSP_LED_Toggle(void){
    bsp_io_level_t level;
    // 读取当前引脚电平
    R_IOPORT_PinRead(&g_ioport_ctrl, LED_PIN, &level);
    // 电平取反输出
    level = (level == BSP_IO_LEVEL_HIGH) ? BSP_IO_LEVEL_LOW : BSP_IO_LEVEL_HIGH;
    R_IOPORT_PinWrite(&g_ioport_ctrl, LED_PIN, level);
}

/*******************************************************************************************************************//**
 * @brief  设置LED指定电平
 * @param  level：BSP_IO_LEVEL_HIGH / BSP_IO_LEVEL_LOW
**********************************************************************************************************************/
void BSP_LED_Set(bsp_io_level_t level)
{
    R_IOPORT_PinWrite(&g_ioport_ctrl, LED_PIN, level);
}
